package testpackage2;

public class Forloopexample4 {
	public static void main(String[] args) {
		for(int i=7;i>=1;i--)
		{
			for(int j=i;j<=7;j++)
			{
				System.out.print("*");
			}
			System.out.println(" ");
		}
		for(int i=1;i<=7;i++)
		{
			for(int j=i;j<=6;j++)
			{
				System.out.print("*");
			}
			System.out.println(" ");
	}
	}
}
